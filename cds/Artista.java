public class Artista {
    private String nome;
    private String anoEstreia;
    private String generoMusical;

    public String getNome() { return nome; }
    public void setNome(String nome) { this.nome = nome; }
    public String getAnoEstreia() { return anoEstreia; }
    public void setAnoEstreia(String anoEstreia) { this.anoEstreia = anoEstreia; }
    public String getGeneroMusical() { return generoMusical; }
    public void setGeneroMusical(String generoMusical) { this.generoMusical = generoMusical; }
}